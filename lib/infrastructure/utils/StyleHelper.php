	public static function getStyle($args){
		$theme = isset($args['theme']) ? $args['theme'] : null;
		switch($theme){
			case 'red':
				return self::Red($args);
			break;
			case 'red_violet':
				return self::RedViolet($args);
			break;
			case 'hollywood_cerise':
				return self::HollywoodCerise($args);
			case 'violet':
				return self::Violet($args);
			break;
			case 'violet_blue':
				return self::VioletBlue($args);
			break;
			case 'blue':
				return self::Blue($args);
			break;
			case 'blue_green':
				return self::BlueGreen($args);
			break;
			case 'green':
				return self::Green($args);
			break;
			case 'green_yellow':
				return self::GreenYellow($args);
			break;
			case 'yellow':
				return self::Yellow($args);
			break;
			case 'yellow_orange':
				return self::YellowOrange($args);
			break;
			case 'orange':
				return self::Orange($args);
			break;
			case 'orange_red':
				return self::OrangeRed($args);
			break;
			case 'gray':
				return self::Gray($args);
			break;
			case 'gray_blue':
				return self::GrayBlue($args);
			break;
		}
		return new Calendarista_Style($args);
	}
